private void crearArchivoInfo(String nombre, String p1, String p2, String p3, String p4, String total,boolean Val) {
        abrirArchivoInfo();
        escribirArchivoInfo(nombre, p1, p2, p3, p4, total,Val);
        cerrarArchivoInfo();
    }

    public static void abrirArchivoInfo() {
        try {
            info = new Formatter("info.txt");
        } catch (FileNotFoundException ex) {
            System.err.println("No existe el archivo");
            System.exit(1);
        } catch (SecurityException ex) {
            System.err.println("El archivo tiene permisos de escritura");
            System.exit(1);
        }
    }

    // agrega registros al archivo
    public static void escribirArchivoInfo(String nombre, String p1, String p2, String p3, String p4, String total,boolean Val) {
        if(Val==true){
            try {
                info.format("%-20s %-20s %-20s %-20s %-20s %-20s%n", "Nombre", "Producto 1", "Producto 2", "Producto 3", "Producto 4", "Total+IVA");
                info.format("%-20s %-20s %-20s %-20s %-20s %-20s%n%n", nombre, p1, p2, p3, p4, total);
            } catch (FormatterClosedException r) {
                System.err.println("Error al abrir el archivo");
            }  
        }else{
         info.format("%-20s %-20s %-20s %-20s %-20s %-20s%n", "Nombre", "Producto 1", "Producto 2", "Producto 3", "Producto 4", "Total+IVA");
                info.format("%-20s %-20s %-20s %-20s %-20s %-20s%n%n", null, null, null, null, null, null);   
        }
        

    }

    public static void cerrarArchivoInfo() {
        if (info != null) {
            info.close();
        }
    }
