    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        try {
            if("".equals(this.jTextField1.getText())||"".equals(this.jTextField2.getText())||"".equals(this.jTextField3.getText())||"".equals(this.jTextField4.getText())||"".equals(this.jTextField5.getText())){
                throw new NumberFormatException();
            }
            else{
                p1 = Double.parseDouble(this.jTextField2.getText());
                p2 = Double.parseDouble(this.jTextField3.getText());
                p3 = Double.parseDouble(this.jTextField4.getText());
                p4 = Double.parseDouble(this.jTextField5.getText());
                compra=p1 + p2 + p3 + p4;
                this.masIVA=compra*1.16;
                this.jTextField6.setText(String.valueOf(masIVA));
                this.validado=true;
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null,"Hay un error en los datos de entrada");
        }
    }//GEN-LAST:event_jButton3MouseClicked

